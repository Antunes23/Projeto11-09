package Dia21_08;

public class cliente {

	//Atributos 
	
	int id;
	String nome;
	int password;
	String permissão;
	String usuario;
	
	// Métodos
	
	String compra;
	String paga;
	String parcela;
	String caloteia;
	
	

	public int getid () {
		return id;
	}
	public void setid (int id) {
		this.id = id;
	}
	public String getnome () {
		return nome;
	}
	public void setnome (String nome) {
		this.nome = nome;
	}
	public int getpassword () {
		return password;
	}
	public void setpassword (int password) {
		this.password = password;
	}
	public String getpermissão () {
		return permissão;
	}
	public void setpermissão (String permissão) {
		this.permissão = permissão;
	}
	public String getusuario () {
		return usuario;
	}
	public void setusuario (String usuario) {
		this.usuario = usuario;
	}
	public String getcompra () {
		return compra;
	}
	public void setcompra (String compra) {
		this.compra = compra;
	}
	public String getpaga () {
		return paga;
	}
	public void setpaga (String paga) {
		this.paga = paga;
	}
	public String getparcela () {
		return parcela;
	}
	public void setparcela (String parcela) {
		this.parcela = parcela;
	}
	public String getcaloteia () {
		return parcela;
	}
	public void setcaloteia (String caloteia) {
		this.caloteia = caloteia;

	}

}
