package Dia21_08;

public class fazenda {
	
		//Atributos
		
		
		String raça;
		String porte;
		String sexo;
		int idade;
		
		
		//Métodos
		
		String dormir;
		String comer;
		String beber;
		String caminhar;
		
		public String getraça () {
			return raça;
		}
		public void setraça (String raça) {
			this.raça = raça;
		}
		public String getporte () {
			return porte;
		}
		public void setporte (String porte) {
			this.porte = porte;
		}
		public String getsexo () {
			return sexo;
		}
		public void setsexo (String sexo) {
			this.sexo = sexo;
		}
		public int getidade () {
			return idade;
		}
		public void setidade (int idade) {
			this.idade = idade;
		}
		public String getdormir () {
			return dormir;
		}
		public void setdormir (String dormir) {
			this.dormir = dormir;
		}
		public String getcomer () {
			return comer;
		}
		public void setcomer (String comer) {
			this.comer = comer;
		}
		public String getbeber () {
			return beber;
		}
		public void setbeber (String beber) {
			this.beber = beber;
		}
		public String getcaminhar () {
			return caminhar;
		}
		public void setcaminhar (String caminhar) {
			this.caminhar = caminhar;
		

	}

}
